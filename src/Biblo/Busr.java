    void Mostrar(){
        int filas=tab1.getRowCount();
        for(int i=0;i<filas;i++){
            tab1.removeRow(0);
        }
        
        for(int i = 0;i<Libro.size();i++){
            Object [] most = {Libro.get(i).titulo,Libro.get(i).autor,Libro.get(i).Año,Libro.get(i).editorial,Libro.get(i).codigo,Libro.get(i).estado};
            tab1.addRow(most);
        }
            
        
        
    }


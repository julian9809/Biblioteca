    private void txFiltrarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txFiltrarKeyTyped
     
        
              
        txFiltrar.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent ke) {
                
                trs.setRowFilter(RowFilter.regexFilter("(?i)"+txFiltrar.getText(), 0));
                if (txFiltrar == null){
                    txFiltrar.remove(0);
                    
                }
                Mostrar();
            }
            });
        trs =new TableRowSorter(tab1);
        Tabla3.setRowSorter(trs);
        
    }//GEN-LAST:event_txFiltrarKeyTyped

